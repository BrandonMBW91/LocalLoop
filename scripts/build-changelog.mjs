// Regenerate src/data/changelog.js from git history.
//
// WHY GENERATED: a hand-kept changelog drifts, and a drifted doc is worse than none —
// this repo shipped a README describing a prototype for a year. Git already knows every
// release: each one is a BUILD bump in src/version.js, and the commits between two bumps
// ARE that rev's changes. So the log is derived, never typed, and cannot go stale.
//
//   node scripts/build-changelog.mjs        # rewrite src/data/changelog.js
//   node scripts/build-changelog.mjs --check # exit 1 if it is out of date (for CI)
//
// ORDER MATTERS: run this AFTER committing the release, not before. It reads COMMITTED
// history, so a rev whose BUILD bump is still sitting in the working tree does not
// exist yet and will be missing from the log. The release order is:
//   1. bump BUILD in src/version.js   2. commit the release
//   3. run this                       4. commit src/data/changelog.js
// The guard in tests/guards.test.mjs fails if you skip step 3, which is how this
// constraint was found rather than shipped.
//
// The output ships inside the JS bundle so the Settings screen can read it offline.
// It is admin-gated in the UI, NOT secret: anyone can read the bundle. Keep commit
// subjects free of anything you would not publish.
import { execFileSync } from 'node:child_process';
import { writeFileSync, readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = join(ROOT, 'src', 'data', 'changelog.js');
// execFileSync with an args array, never a shell string: git format specifiers contain
// '|' and '%', which cmd.exe on Windows eats as a pipe and a variable marker.
const git = (...args) => execFileSync('git', args, { cwd: ROOT, encoding: 'utf8', maxBuffer: 32 * 1024 * 1024 });

// Every commit that changed BUILD, oldest first, with the value it set.
const bumps = [];
{
  const log = git('log', '--reverse', '--format=%x00%H|%ad|%s', '--date=short', '-p', '--', 'src/version.js');
  for (const chunk of log.split('\x00').slice(1)) {
    const [header, ...rest] = chunk.split('\n');
    const [sha, date, subject] = header.split('|');
    const body = rest.join('\n');
    const build = /^\+export const BUILD = (\d+)/m.exec(body);
    if (!build) continue; // a commit touching version.js without moving BUILD (e.g. WHATS_NEW only)
    const whatsNew = /^\+export const WHATS_NEW = ["'`]([\s\S]*?)["'`];/m.exec(body);
    bumps.push({ sha, date, subject, rev: Number(build[1]), whatsNew: whatsNew ? whatsNew[1] : null });
  }
}

// The changes in rev N are the commits between the bump to N-1 and the bump to N.
const entries = [];
for (let i = 0; i < bumps.length; i++) {
  const cur = bumps[i], prev = bumps[i - 1];
  let subjects = [];
  try {
    const range = prev ? `${prev.sha}..${cur.sha}` : cur.sha;
    subjects = git('log', '--format=%s', range)
      .split('\n')
      .map((s) => s.trim())
      .filter(Boolean)
      // Drop noise that tells the reader nothing about the product.
      .filter((s) => !/^(Merge |Bump |chore|wip\b)/i.test(s))
      // The trailing "- rev 102" / "(rev 95" is already the heading here.
      .map((s) => s.replace(/[\s(-]*\brev \d+\)?\.?$/i, '').trim())
      .filter(Boolean);
  } catch { /* first commit, or a rewritten range */ }
  entries.push({ rev: cur.rev, date: cur.date, whatsNew: cur.whatsNew, changes: [...new Set(subjects)] });
}
entries.reverse(); // newest first — what you want to read

const body = `// GENERATED by scripts/build-changelog.mjs — do not hand-edit.
// Regenerate:  node scripts/build-changelog.mjs
//
// Derived from git: each entry is a BUILD bump in src/version.js, and its \`changes\`
// are the commit subjects since the previous bump. Read by app/changelog.js, which is
// admin-gated in the UI but ships in the public bundle — it is a convenience, not a
// secret. ${entries.length} revs, newest first.
export const CHANGELOG = ${JSON.stringify(entries, null, 2)};
`;

if (process.argv.includes('--check')) {
  const current = (() => { try { return readFileSync(OUT, 'utf8'); } catch { return ''; } })();
  if (current.trim() !== body.trim()) {
    console.error('src/data/changelog.js is out of date. Run: node scripts/build-changelog.mjs');
    process.exit(1);
  }
  console.log(`changelog is current (${entries.length} revs)`);
  process.exit(0);
}

writeFileSync(OUT, body);
const withNotes = entries.filter((e) => e.changes.length).length;
console.log(`src/data/changelog.js written: ${entries.length} revs (rev ${entries.at(-1)?.rev}..${entries[0]?.rev}), ${withNotes} with commit notes`);
